import type { BreedFAQ, Dog, DogCharacteristics, HealthInfo } from '@/types';
import { siteConfig } from './site-config';

interface BreedSeed {
  breedName: string;
  categorySlug: string;
  status: Dog['status'];
  gender: Dog['gender'];
  age: string;
  featured: boolean;
  summary: string;
  characteristics: DogCharacteristics;
  tags: string[];
  localImagePath: string;
}

function toSlug(value: string): string {
  return value
    .toLowerCase()
    .replace(/[^a-z0-9\s-]/g, '')
    .trim()
    .replace(/\s+/g, '-');
}

function buildHealthInfo(seed: BreedSeed): HealthInfo {
  // Customer mentioned: "Not all the puppies are KCI registered please rec correct"
  const kciRegistered = seed.tags.includes('KCI Registered');

  return {
    vaccinated: true,
    dewormed: true,
    vetChecked: true,
    microchipped: false,
    kciRegistered,
    parentsCertified: seed.tags.includes('Champion Lineage'),
  };
}

function buildFaqs(
  breedName: string,
  apartmentFriendly: boolean,
  kciRegistered: boolean
): BreedFAQ[] {
  return [
    {
      question: `What is the price of ${breedName} puppy in Bangalore?`,
      answer: `The price depends on the specific puppy and bloodline. Please contact Richard at ${siteConfig.brandName} (${siteConfig.phone}) for current pricing and availability.`,
    },
    {
      question: `Is this ${breedName} puppy KCI registered?`,
      answer: kciRegistered
        ? `This ${breedName} listing is KCI registered. Contact us to confirm the paperwork.`
        : siteConfig.registrationNote,
    },
    {
      question: `Is ${breedName} good for Bangalore climate?`,
      answer: `Yes, ${breedName} adapts well to Bengaluru weather with proper hydration, nutrition, and routine grooming.`,
    },
    {
      question: `Is ${breedName} apartment-friendly in Bangalore?`,
      answer: apartmentFriendly 
        ? `Yes, ${breedName} is very apartment-friendly when given daily walks.` 
        : `${breedName} is better suited to homes with more space, but can adapt to large apartments with structured exercise.`,
    },
  ];
}

